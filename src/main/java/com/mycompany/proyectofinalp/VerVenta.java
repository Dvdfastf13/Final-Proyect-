    private void pintarTabla() {
        DefaultTableModel vr = new DefaultTableModel(new String[]{"Vendedor","Libro","Cantidad","Precio Unit","Total + IVA"},ProyectoFinalP.dlibros.size());
        verventa.setModel(vr);
        
        TableModel tabla = verventa.getModel();
        for(int i = 0; i<ProyectoFinalP.dlibros.size();i++){
            Dlibros l = ProyectoFinalP.dlibros.get(i);
            tabla.setValueAt(l.getTitulo(), i, 1);
            tabla.setValueAt(l.getCantidad(), i, 2);
            tabla.setValueAt(l.getPrecio(), i, 3);
            double totaliva = l.getCantidad() * l.getPrecio() *1.12;
            tabla.setValueAt(totaliva, i, 4);
        }
  
    }
    
