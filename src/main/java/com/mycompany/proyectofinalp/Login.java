    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
        
        String usuario = user.getText();
        String passw = password.getText();
        
        boolean existe = false;
        
            for(Usuario u : ProyectoFinalP.usuarios){
                if(u.getUsuario().equals(usuario) && u.getPassword().equals(passw)){
                existe = true;
                if(u.getRol() == 1){
                   Administrador v = new Administrador();
                   v.setVisible(true);
                }
                else{
                    Vendedor v = new Vendedor(u);
                    v.setVisible(true);
                }
            }
        }
        
        if(existe == false){
            JOptionPane.showMessageDialog(this, "Usuario o password incorrecto");
        }

        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

