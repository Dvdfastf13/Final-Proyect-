    private void escribirxml(File documento){
                
        FileWriter scr = null;
        PrintWriter es = null;
        
        
        try{
            
            scr = new FileWriter(documento);
            es = new PrintWriter(scr);
            
            es.println("<usuarios>");
            
            for(Usuario u : usuarios){     
                
                es.println("<usuario>");
                
                es.println("<user>"+u.getUsuario()+"</user>");
                es.println("<nombre>"+u.getNombre()+"</nombre>");
                es.println("<password>"+u.getPassword()+"</password>");
                es.println("<rol>"+u.getRol()+"</rol>");
                
                es.println("</usuario>");

                
            }
            
            
            
            es.println("</usuarios>");

            
            
        }catch(Exception e){
            e.printStackTrace();
            
        }
        finally{
            try {
                if(scr!=null){
                    scr.close();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
