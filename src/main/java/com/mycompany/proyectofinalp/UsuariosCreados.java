private void pintarTabla(){
       
        
        DefaultTableModel t = new DefaultTableModel(new String[]{"Nombre","Usuario","Password","Rol"},ProyectoFinalP.usuarios.size());
        RegUsuarios.setModel(t);
        
        TableModel tabla = RegUsuarios.getModel();
        
        for(int i = 0; i<ProyectoFinalP.usuarios.size();i++){
            Usuario u = ProyectoFinalP.usuarios.get(i);
            tabla.setValueAt(u.getNombre(), i, 0);
            tabla.setValueAt(u.getUsuario(), i, 1);
            tabla.setValueAt(u.getPassword(), i, 2);
            tabla.setValueAt(u.getRol(), i, 3);
        }
    }
    

