    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(cupones!=null){
            cupones.setCodigo(Integer.parseInt(codigo1.getText()));
            cupones.setValor(Integer.parseInt(valor1.getText()));
            cupones.setFecha(fecha1.getText());
            String tipoo = tipo1.getSelectedItem().toString();
            
            if(tipoo.equals("monto fijo")){
                cupones.setTipo(1);
            }
            else{
                cupones.setTipo(2);
            }
            pintarTabla();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

