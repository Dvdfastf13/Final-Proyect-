private void pintarTabla(){
        
    DefaultTableModel li = new DefaultTableModel(new String[]{"codigo","valor","tipo","fecha de vencimiento"},ProyectoFinalP.cuppones.size());
    ccupon.setModel(li);
    
    TableModel tabla = ccupon.getModel();
    
        for(int i = 0; i<ProyectoFinalP.cuppones.size();i++){
        Cupones cb  = ProyectoFinalP.cuppones.get(i);
        tabla.setValueAt(cb.getCodigo(),i , 0);
        tabla.setValueAt(cb.getValor(), i, 1);
        tabla.setValueAt(cb.getTipo(), i, 2);
        tabla.setValueAt(cb.getFecha(), i, 3);
    
        }  
    }

