    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         int modificar = ccupon.getSelectedRow();
         if(modificar >-1){
        cupones = ProyectoFinalP.cuppones.get(modificar);
        codigo1.setText(Integer.toString(cupones.getCodigo()));
        valor1.setText(Integer.toString(cupones.getValor()));
        fecha1.setText(cupones.getFecha());
        tipo1.setSelectedIndex(cupones.getTipo() -1);    
    }
         else{
             JOptionPane.showMessageDialog(this, "elija un cupon");
         }
    
    
    }//GEN-LAST:event_jButton3ActionPerformed

