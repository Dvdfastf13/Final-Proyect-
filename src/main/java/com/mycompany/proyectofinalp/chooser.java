    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        JFileChooser AbrirArchivo = new JFileChooser();
        FileNameExtensionFilter TipoArchivo = new FileNameExtensionFilter("Archivo en csv", "csv");
        
        AbrirArchivo.setFileFilter(TipoArchivo);
        
        int abrir = AbrirArchivo.showOpenDialog(this);
        
        if(abrir == JFileChooser.APPROVE_OPTION){
            File archivo =  AbrirArchivo.getSelectedFile();
            leerArchivo(archivo);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public void leerArchivo(File archivo){
        
        FileReader fr = null;
        BufferedReader br = null;
        
        try{
            fr = new FileReader(archivo);
            br = new BufferedReader(fr);
            
            String linea;
            
            while((linea=br.readLine())!=null){
                
                String arreglo[] = linea.split("\\|");
                if(arreglo.length==4){
                    Cupones c = new Cupones();
                    c.setCodigo(Integer.parseInt(arreglo[0]));
                    c.setTipo(Integer.parseInt(arreglo[1]));
                    c.setValor(Integer.parseInt(arreglo[2]));
                    c.setFecha(arreglo[3]);
                    
                    cupones.add(c);
                    
                }
                
                
            }
            
            pintarTabla();
            
            
        }catch(Exception ex){
            
        }
        pintarTabla();
    }
    


