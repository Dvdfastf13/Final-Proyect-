       private void pintarTabla() {
         
         DefaultTableModel md = new DefaultTableModel(new String[]{"Tipo","Valor","Codigo","fecha"},cupones.size());
         cucucu.setModel(md);
         
         TableModel tm = cucucu.getModel();
         
         for(int i = 0; i<cupones.size();i++){
             
             Cupones c = cupones.get(i);
             tm.setValueAt(c.getTipo(), i, 0);
             tm.setValueAt(c.getValor(), i, 1);
             tm.setValueAt(c.getCodigo(), i, 2);
             tm.setValueAt(c.getFecha(), i, 3);
             
         }
       }

